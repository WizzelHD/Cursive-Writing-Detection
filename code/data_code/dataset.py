import os
from pathlib import Path
from PIL import Image
from torch.utils.data import Dataset, ConcatDataset
import torchvision.transforms as transforms
import torch

class IAMWordDataset(Dataset):
    def __init__(self, labels_dict, words_root, encoder, transform=None):
        self.words_root = words_root
        self.encoder = encoder
        
        # speichern von ids und texten
        self.samples = list(labels_dict.items())

        if transform is None:
            self.transform = transforms.Compose([
                transforms.Resize((32, 128)),
                transforms.ToTensor()
            ])
        else:
            self.transform = transform
            
        print(f"Dataset initialized with {len(self.samples)} samples.")

    def __len__(self):
        return len(self.samples)

    def _get_path(self, image_id):
        parts = image_id.split("-")
        folder_1 = parts[0]
        folder_2 = parts[0] + "-" + parts[1]
        return os.path.join(
            self.words_root,
            folder_1,
            folder_2,
            image_id + ".png"
        )

    def __getitem__(self, idx):
        image_id, text = self.samples[idx]

        try:
            image_path = self._get_path(image_id)
            
            image = Image.open(image_path).convert("L")

            if self.transform:
                image = self.transform(image)

            encoded = self.encoder.encode(text)
            encoded = torch.tensor(encoded, dtype=torch.long)
            
            return image, encoded, len(encoded)

        except Exception:
            # Wenn das Bild korrupt ist oder fehlt geben wir None zurück.
            # Die collate_fn filtert dies dann aus dem Batch heraus
            return None

class SyntheticWordDataset(Dataset):
    """
    Loads synthetic word images generated by generate_synthetic.py.
    Images live in a flat directory: <words_root>/<image_id>.png
    Compatible with crnn_collate_fn (returns image, encoded, length).
    """
    def __init__(self, labels_dict: dict, words_root: str | Path, encoder, transform=None):
        self.words_root = Path(words_root)
        self.encoder = encoder
        self.samples = list(labels_dict.items())

        if transform is None:
            self.transform = transforms.Compose([
                transforms.Resize((32, 128)),
                transforms.ToTensor()
            ])
        else:
            self.transform = transform

        print(f"SyntheticWordDataset initialized with {len(self.samples)} samples.")

    def __len__(self):
        return len(self.samples)

    def __getitem__(self, idx):
        image_id, text = self.samples[idx]
        try:
            image_path = self.words_root / f"{image_id}.png"
            image = Image.open(image_path).convert("L")
            if self.transform:
                image = self.transform(image)
            encoded = self.encoder.encode(text)
            encoded = torch.tensor(encoded, dtype=torch.long)
            return image, encoded, len(encoded)
        except Exception:
            return None


def crnn_collate_fn(batch):
    # entferne alle None-Einträge aus dem Batch
    batch = [sample for sample in batch if sample is not None]
    
    # Falls der gesamte Batch leer ist
    if len(batch) == 0:
        return None

    # restlichen Batch verarbeiten
    images, labels, lengths = zip(*batch)

    images = torch.stack(images)
    labels = torch.nn.utils.rnn.pad_sequence(
        labels,
        batch_first=True,
        padding_value=0
    )
    lengths = torch.tensor(lengths, dtype=torch.long)

    return images, labels, lengths

def resnet_collate_fn(batch):
    # None-Einträge filtern
    batch = [sample for sample in batch if sample is not None]
    if len(batch) == 0:
        return None

    #  Entpacken (resnet liefert nur 2 Werte)
    images, targets = zip(*batch)

    # 3. Stapeln
    images = torch.stack(images)
    targets = torch.tensor(targets, dtype=torch.long) # Einfacher Tensor statt Padding

    return images, targets